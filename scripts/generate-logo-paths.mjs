import opentype from 'opentype.js';
import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Converts the "kelimeki" wordmark from live Caveat text into static SVG
// glyph outlines (<path> data), so the in-app logo (LogoMark.tsx) never
// depends on the Caveat web font loading at all — no @font-face, no
// network request, no font-display swap, no FOUT, ever. Mirrors the exact
// geometry generate-logo.mjs uses for the exported logo.svg/.png/.jpg
// (same font file, size, letter-spacing, underline swash) so the result
// is visually identical to the existing exported assets.

// .woff (not .woff2) — opentype.js can't parse WOFF2 without an external
// decompressor; plain WOFF is a simple zlib-wrapped sfnt it reads natively,
// and it's the exact same Caveat Bold glyph outlines either way.
const fontPath = join(__dirname, '../node_modules/@fontsource/caveat/files/caveat-latin-700-normal.woff');
const fontBuffer = readFileSync(fontPath);
const fontArrayBuffer = fontBuffer.buffer.slice(fontBuffer.byteOffset, fontBuffer.byteOffset + fontBuffer.byteLength);
const font = opentype.parse(fontArrayBuffer);

const TEXT = 'kelimeki';
const LOGO_COLOR = '#2563EB';
const FONT_SIZE = 280;
const LETTER_SPACING = 30;
const TEXT_Y = 300;
const CANVAS_WIDTH = 1400;
const UNDERLINE_Y = 330;
const UNDERLINE_WIDTH = 640;
const UNDERLINE_PATH = 'M20 30 Q160 10 320 30 Q480 50 620 30';
const UNDERLINE_STROKE_WIDTH = 20;
const PAD = 20;

const letterSpacingOption = { letterSpacing: LETTER_SPACING / FONT_SIZE };
const textWidth = font.getAdvanceWidth(TEXT, FONT_SIZE, letterSpacingOption);
const startX = CANVAS_WIDTH / 2 - textWidth / 2;
const textPath = font.getPath(TEXT, startX, TEXT_Y, FONT_SIZE, letterSpacingOption);
const textPathData = textPath.toPathData(2);
const textBBox = textPath.getBoundingBox();

const underlineOffsetX = (CANVAS_WIDTH - UNDERLINE_WIDTH) / 2;
// UNDERLINE_PATH's own coordinates span roughly x:[4,96] y:[1,7] (see
// generate-logo.mjs) — half the stroke width is added as a rough margin
// since a stroked path's ink extends beyond its coordinate path.
const underlineBBox = {
  x1: underlineOffsetX + 4 - UNDERLINE_STROKE_WIDTH / 2,
  x2: underlineOffsetX + 96 + UNDERLINE_STROKE_WIDTH / 2,
  y1: UNDERLINE_Y + 1 - UNDERLINE_STROKE_WIDTH / 2,
  y2: UNDERLINE_Y + 7 + UNDERLINE_STROKE_WIDTH / 2,
};

const bbox = {
  x1: Math.min(textBBox.x1, underlineBBox.x1) - PAD,
  y1: Math.min(textBBox.y1, underlineBBox.y1) - PAD,
  x2: Math.max(textBBox.x2, underlineBBox.x2) + PAD,
  y2: Math.max(textBBox.y2, underlineBBox.y2) + PAD,
};
const viewBox = [
  bbox.x1.toFixed(2),
  bbox.y1.toFixed(2),
  (bbox.x2 - bbox.x1).toFixed(2),
  (bbox.y2 - bbox.y1).toFixed(2),
].join(' ');

const tsx = `// AUTO-GENERATED by scripts/generate-logo-paths.mjs — do not hand-edit.
// "kelimeki" wordmark as static SVG glyph outlines (Caveat Bold traced to
// paths) instead of live text — renders instantly with the rest of the
// React tree, no font loading/FOUT/network request involved (see
// src/fonts/ removal note in CLAUDE.md for why this replaced the old
// @font-face-based logo).
export const LOGO_VIEW_BOX = '${viewBox}';
export const LOGO_TEXT_PATH = '${textPathData}';
export const LOGO_UNDERLINE_PATH =
  'M${underlineOffsetX + 20} ${UNDERLINE_Y + 30} Q${underlineOffsetX + 160} ${UNDERLINE_Y + 10} ${underlineOffsetX + 320} ${UNDERLINE_Y + 30} Q${underlineOffsetX + 480} ${UNDERLINE_Y + 50} ${underlineOffsetX + 620} ${UNDERLINE_Y + 30}';
export const LOGO_COLOR = '${LOGO_COLOR}';
export const LOGO_UNDERLINE_STROKE_WIDTH = ${UNDERLINE_STROKE_WIDTH};

interface LogoMarkProps {
  height: number;
  color?: string;
  className?: string;
}

export function LogoMark({ height, color = LOGO_COLOR, className }: LogoMarkProps) {
  const [, , vbWidth, vbHeight] = LOGO_VIEW_BOX.split(' ').map(Number);
  const width = (height * vbWidth) / vbHeight;
  return (
    <svg
      width={width}
      height={height}
      viewBox={LOGO_VIEW_BOX}
      fill="none"
      aria-hidden="true"
      className={className}
    >
      <path d={LOGO_TEXT_PATH} fill={color} />
      <path
        d={LOGO_UNDERLINE_PATH}
        stroke={color}
        strokeWidth={LOGO_UNDERLINE_STROKE_WIDTH}
        strokeLinecap="round"
        fill="none"
      />
    </svg>
  );
}
`;

writeFileSync(join(__dirname, '../src/components/LogoMark.tsx'), tsx);
console.log('✓ src/components/LogoMark.tsx (vector wordmark, no font dependency)');
